    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        Long idContrato = Long.parseLong(jTextField8.getText());
        contrato = controlador.ObtenerContrato(idContrato);
        if(contrato!=null){
            Inmueble inmueble = controlador.BuscarInmueble(String.valueOf(contrato.getIdinmueble()));
            if(contrato.getTipocontrato() == 1){
                alquiler = controlador.ObtenerAlquiler(idContrato);
                jPanel3.setVisible(true);
                String matriz[][] = controlador.ObtenerMatrizServicio(alquiler.getServicios());
                if(alquiler.getServicios()==null){

                }else{
                    for(int i=0;alquiler.getServicios().size()>i;i++){
                        modelo.addRow(matriz[i]);
                    }
                }
                String matrizalqui[][] = controlador.CrearMatrizArancelVista(alquiler.getArancels());
                jTable5.setModel(arancel);
                for(int j=0;alquiler.getArancels().size()>j;j++){
                    arancel.addRow(matrizalqui[j]);
                }
                jTextField12.setText("Alquiler");
                jTextField4.setText(String.valueOf(alquiler.getDnigarante()));
            }
            if(contrato.getTipocontrato()== 2){
                venta = controlador.ObtenerVenta(idContrato);
                jPanel3.setVisible(false);
                String matrizventa[][] = controlador.CrearMatrizArancelVista(venta.getArancels());
                jTable5.setModel(arancel);
                for(int j=0;venta.getArancels().size()>j;j++){
                    arancel.addRow(matrizventa[j]);
                }
                jTextField12.setText("Venta");
            }
            if(inmueble.getTipo()==1){
                jTextField13.setText("Casa");
            }
            if(inmueble.getTipo()==2){
                jTextField13.setText("Departamento");
            }
            if(inmueble.getTipo()==3){
                jTextField13.setText("Local Comercial");
            }
            jTextField1.setText(String.valueOf(contrato.getIdinmueble()));
            jTextField2.setText(String.valueOf(contrato.getDnilocador()));
            jTextField3.setText(String.valueOf(contrato.getDnilocatario()));
            if(contrato.getCantaranceles()>1){
                jTextField14.setText(String.valueOf(contrato.getCantaranceles())+" Meses");
            }else if(contrato.getCantaranceles()==1){
                jTextField14.setText("1 Mes");
            }
            jTextField5.setText(String.valueOf(contrato.getRecargofijo()));
            jTextField6.setText(String.valueOf(contrato.getValorbaserecargo()));
            jTextField7.setText(String.valueOf(contrato.getMontocomision()));
            DateFormat fecha = new SimpleDateFormat("dd/MM/yyyy");
            String inicioText = fecha.format(contrato.getIniciocontrato());
            String finText = fecha.format(contrato.getFincontrato());
            jTextField10.setText(inicioText);
            jTextField9.setText(finText);
        }else{
            JOptionPane.showMessageDialog(null, "No hay contrato con ese ID");
        }
    }//GEN-LAST:event_button1ActionPerformed

