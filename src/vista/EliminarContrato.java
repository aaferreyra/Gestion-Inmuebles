    private void button2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button2ActionPerformed
        if(contrato != null){
            if(alquiler != null){
                List<Arancel> ara = alquiler.getArancels();
                alquiler.setArancels(null);
                alquiler.setServicios(null);
                controlador.ActualizarAlquiler(alquiler);
                for(Arancel a: ara){
                    controlador.EliminarArancel(a);
                }
                controlador.EliminarAlquiler(alquiler);
            }    
            if(venta != null){
                List<Arancel> ara = venta.getArancels();
                venta.setArancels(null);
                controlador.ActualizarVenta(venta);
                for(Arancel a: ara){
                    controlador.EliminarArancel(a);
                }
                controlador.EliminarVenta(venta);
            }
            for (int i = 0; i < jTable5.getRowCount(); i++) {
                arancel.removeRow(i);
                i-=1;
            }
            for (int i = 0; i < jTable3.getRowCount(); i++) {
                modelo.removeRow(i);
                i-=1;
            }
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
            jTextField7.setText("");
            jTextField8.setText("");
            jTextField9.setText("");
            jTextField10.setText("");
            jTextField12.setText("");
            jTextField13.setText("");
            jTextField13.setText("");
           
        }
        
    }//GEN-LAST:event_button2ActionPerformed

